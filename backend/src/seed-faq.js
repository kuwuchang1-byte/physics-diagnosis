export function seedFAQ() {
  const existing = db.prepare('SELECT COUNT(*) as count FROM faq_entries').get();
  if (existing.count > 0) {
    console.log(`FAQ 表已有 ${existing.count} 条数据，跳过种子填充。`);
    return;
  }

  const stmt = db.prepare(
    'INSERT INTO faq_entries (question, answer, category, tags) VALUES (?, ?, ?, ?)'
  );

  db.exec('BEGIN TRANSACTION');
  for (const item of FAQ_DATA) {
    stmt.run(item.question, item.answer, item.category, item.tags);
  }
  db.exec('COMMIT');
  console.log(`成功插入 ${FAQ_DATA.length} 条 FAQ 数据。`);
}

// 如果直接运行此文件则执行播种
if (process.argv[1]?.endsWith('seed-faq.js')) {
  seedFAQ();
  console.log('FAQ 播种完成！');
}
